% Clear the output window and suppress any warning statements
clc
warning('off', 'all');
%-----------------------------------------------------------------------------
% Begin the program
%-----------------------------------------------------------------------------
disp("This program is a simulation A gravitational dip for non-pulsating heavenly body by Rajesh Kumar Das(I17PH015)")
pause(1)
%-----------------------------------------------------------------------------
% Define the values of variables
%-----------------------------------------------------------------------------
x = linspace(-3, 3, 50);
y = linspace(-3, 3, 50);
%-----------------------------------------------------------------------------
% Determing the value of individual points
%-----------------------------------------------------------------------------
[X, Y] = meshgrid(x, y);
Z = -10 ./ (X.^2 + Y.^2 + 1);
%-----------------------------------------------------------------------------
% Plotting the surface
%-----------------------------------------------------------------------------
surf(Z, 'facecolor', 'b', 'edgecolor', 'k')
hold on
%-----------------------------------------------------------------------------
% Determing the value of individual points
%-----------------------------------------------------------------------------
[a, b, c] = sphere(30);
%-----------------------------------------------------------------------------
% Plotting the sphere
%-----------------------------------------------------------------------------
surf(a * 6 + 25, b * 6 + 25, c * 6 - 1, 'facecolor', 'r', 'edgecolor', 'w')
axis ([0 50 0 50 -25 25]);
